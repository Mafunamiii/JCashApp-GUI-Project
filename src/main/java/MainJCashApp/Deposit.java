    private void depositAMTTFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_depositAMTTFieldKeyPressed
        char value = evt.getKeyChar();

        if (value >= KeyEvent.VK_0 && value <= KeyEvent.VK_9 || value == KeyEvent.VK_BACK_SPACE) {

            depositAMTTField.setEditable(true);
        } else if ( value == KeyEvent.VK_ENTER ) {
            enteredAmount = Integer.valueOf(depositAMTTField.getText());
            
            balanceOutput.setText(String.valueOf((accs.getAcct2().getSavings() + enteredAmount)));
            
        } else {
            depositAMTTField.setEditable(false);
            JOptionPane.showMessageDialog(this, "Enter numbers 0-9 only");
        }
    }//GEN-LAST:event_depositAMTTFieldKeyPressed

